drop table if exists asientos;
drop table if exists pagos;
drop table if exists reservas;
drop table if exists pasajeros;
drop table if exists vuelos;

create table vuelos(
id_vuelo int identity(1,1) primary key,
origen varchar(25),
destino varchar(25),
fecha_salida date,
fecha_llegada date,
aerolinea varchar(25)
);
create table pasajeros(
id_pasajero int identity(1,1) primary key,
nombre_apellido varchar(30),
pasaporte varchar(25),
nacionalidad varchar(25),
telefono varchar(25)
);
create table reservas(
id_reserva int identity(1,1) primary key,
id_vuelo int,
id_pasajero int,
asiento varchar(10),
fecha_reserva date,

foreign key (id_vuelo) references vuelos(id_vuelo),
foreign key (id_pasajero) references pasajeros(id_pasajero)
);
create table pagos(
id_pago int identity(1,1) primary key,
id_reserva int,
metodo_pago varchar(25),
monto decimal(10,2),
validacion_pago bit,

foreign key (id_reserva) references reservas(id_reserva)
);
create table asientos(
id_asiento int identity (800,1) primary key,
id_vuelo int,
clase varchar(20),
disponibilidad varchar(20),

foreign key (id_vuelo) references vuelos(id_vuelo)
);

